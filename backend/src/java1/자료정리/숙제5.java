package java1.자료정리;

public class 숙제5 {
public static void main(String[] args) {
	
	byte value1 = 100;				
	short value2 = 30000;			
	int value3 = 2000000000;		
	long value4 = 40000000000L;		
	float value5 = 3.123123123F;
	
	
	
	/* 문제풀이 위치 */
	
	short 쇼트변수 = value1; System.out.println("byte -> short :"+쇼트변수);
	int 인트변수 = value2; System.out.println("short -> int :"+인트변수);
	long 롱변수 = value3; System.out.println("int -> long :"+롱변수);
	float 플룻 = value4; System.out.println("long -> float :"+플룻);
	double 더블 = value5; System.out.println("float -> double :"+더블);
	
	
	/* ----------- */
	
}
}
